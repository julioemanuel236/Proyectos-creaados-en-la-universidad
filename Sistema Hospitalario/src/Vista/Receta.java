    private void btnNuevoMedicamentoAmbActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoMedicamentoAmbActionPerformed
        // TODO add your handling code here:
        try{
            //Atributos de la receta:
            this.medicamento = this.txtMedicamento.getText();
            this.via = this.txtVia.getText();
            this.dosis = this.txtDosis.getText();
            this.frecuencia = Integer.parseInt(this.txtFrecuencia.getText());
            
            //CreaciÃ³n de la clase Receta:
            ClaseRecetas receta1 = new ClaseRecetas(this.medicamento, this.dosis, this.frecuencia, this.via);
            
            //la recetase enviarÃ¡ al objeto de reporte consulta si el paciente es de tipo ambulatorio
            if (Ventana2.validador == true){
                VentanaReporteAmbulatorio.reporteConsulta.getListaRecetas().add(receta1);
            }
            //La receta se enviarÃ¡ al reporte de de internado o a quirurgico dependiendo de cual se haya creado
            else{
                if(Ventana3.validador == true){
                    VentanaReporteInterno.reporteInternado.getListaRecetas().add(receta1);
                }
                else{
                    VentanaReporteQuirurgico2.reporteQuirurjico.getListaRecetas().add(receta1);
                }
            }
            
            JOptionPane.showMessageDialog(rootPane, "Receta registrada\nIngrese una nueva receta o presione omitr para salir");
        }
        catch(ClassCastException e1){
            JOptionPane.showMessageDialog(rootPane, "Tipo de dato incorrecto");
        }
        catch(NumberFormatException e2){
            JOptionPane.showMessageDialog(rootPane, "Tipo de dato incorrecto");
        }
        
    }//GEN-LAST:event_btnNuevoMedicamentoAmbActionPerformed

