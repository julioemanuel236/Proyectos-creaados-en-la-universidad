    private void aceptarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptarButtonActionPerformed
        
        /*
            se crea un transporte nuevo con la informacion
            primero se revisa que el codigo sea valido
            si no lo es se da error
            en caso de serlo se intenta agregar a la lista
            de transportes, si no es posbile es debido
            a que el codgio ya esta siendo usado
            en dicho caso se informa tambien al usuario
            si no hay problemas se agrega a la lista y se actualiza
            la tabla de transportes
        */
        Transporte t = null;
        String codigo = jTextField1.getText();
        if(!Transporte.codigoValido(codigo)){
            JOptionPane.showMessageDialog(null,"CODIGO NO VALIDO");
            return;
        }
        String tipo = (String)jComboBox1.getSelectedItem();
        
        if(!biciButton.isEnabled()){
            int cambios = jComboBox3.getSelectedIndex()+1;
            t = new Bicicleta(codigo,tipo,cambios);
        }
        else if (!scooterButton.isEnabled()){
            String color = jTextField2.getText();
            t = new Scooter(codigo,tipo,color);
        }
        else if(!patineteButton.isEnabled()){
            int ruedas = jComboBox2.getSelectedIndex()+1;
            t = new Patinete(codigo,tipo,ruedas);
        }
        int result = datos.agregarTransporte(t);
        if(result == 0){
            mainWindow.updateTransporteTable();
            dispose();
        }       
        else if(result == 1){
            JOptionPane.showMessageDialog(null,"Codigo ya usado");
        }
        
    }//GEN-LAST:event_aceptarButtonActionPerformed


