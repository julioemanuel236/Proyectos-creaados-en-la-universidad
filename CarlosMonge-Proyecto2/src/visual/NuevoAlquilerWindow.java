    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        /*
            una vez seleccionado el transporte y la distancia
            agregar la informacio al panel final de factura
            y mostrar al usuario
            si no se ha agregado agregarlo
        */
        nombreLabel.setText(cliente.getNombre());
        idLabel.setText(cliente.getId());
        transporteLabel.setText(this.transporte.getCodigo());
        montoLabel.setText("$"+this.transporte.calcularMonto(distancia));
        if(jTabbedPane1.getTabCount() == 2)
            jTabbedPane1.addTab("FACTURA", facturaPanel);        
        jTabbedPane1.setSelectedIndex(2);
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        //al aceptar la factura crearla y agregarla 
        Factura factura = new Factura(System.currentTimeMillis()+"", this.transporte, cliente, distancia, this.transporte.calcularMonto(distancia),System.currentTimeMillis());
        datos.agregarFactura(factura);
        //actualizar la tabla
        mainWindow.updateAlquilerTable();
        //cerrar la ventana
        dispose();
    }//GEN-LAST:event_jButton4ActionPerformed

