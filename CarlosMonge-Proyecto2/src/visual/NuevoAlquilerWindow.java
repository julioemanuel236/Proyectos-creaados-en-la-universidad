        this.cliente = new Cliente(nombreTextField.getText(),
                                   idTextField.getText(),
                                   direccionTextField.getText(),
                                   (int)edadSelector.getValue());
        //si no se ha agregado la siguiente ventana agregarla
        // y cambiar a esta
